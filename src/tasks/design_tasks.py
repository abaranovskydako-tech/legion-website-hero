from crewai import Task

class DesignTasks:
    def analyze_current_hero(self):
        """Анализ текущего hero блока"""
        return Task(
            description="""Проанализируйте текущий hero блок главной страницы:
            1. Оцените текущую структуру и расположение элементов
            2. Определите проблемы с наложением текста и изображений
            3. Проанализируйте пользовательский опыт
            4. Оцените адаптивность на разных устройствах
            5. Создайте список необходимых изменений""",
            agent=self.ui_ux_designer,
            expected_output="Анализ текущего hero блока с выявленными проблемами"
        )
    
    def optimize_hero_layout(self):
        """Оптимизация макета hero блока"""
        return Task(
            description="""Оптимизируйте макет hero блока согласно требованиям:
            1. Разместите фото выше текста и форм захвата
            2. Обеспечьте правильную иерархию элементов
            3. Улучшите читаемость текста
            4. Оптимизируйте формы для лучшей конверсии
            5. Создайте адаптивный дизайн для всех устройств""",
            agent=self.ui_ux_designer,
            expected_output="Оптимизированный макет hero блока"
        )
    
    def optimize_hero_block(self):
        """Полная оптимизация hero блока"""
        return Task(
            description="""Проведите полную оптимизацию hero блока:
            1. Анализ текущего состояния
            2. Создание нового макета
            3. Внедрение изменений
            4. Тестирование на разных устройствах
            5. Финальная проверка качества""",
            agent=self.ui_ux_designer,
            expected_output="Полностью оптимизированный hero блок"
        )
    
    def responsive_design_check(self):
        """Проверка адаптивного дизайна"""
        return Task(
            description="""Проверьте адаптивность дизайна:
            1. Тестирование на мобильных устройствах
            2. Проверка на планшетах
            3. Анализ на десктопе
            4. Проверка различных браузеров
            5. Создание отчета по адаптивности""",
            agent=self.ui_ux_designer,
            expected_output="Отчет по адаптивности дизайна с рекомендациями"
        )
    
    def user_experience_optimization(self):
        """Оптимизация пользовательского опыта"""
        return Task(
            description="""Оптимизируйте пользовательский опыт:
            1. Анализ пользовательских сценариев
            2. Улучшение навигации
            3. Оптимизация форм и кнопок
            4. Улучшение доступности (accessibility)
            5. Создание интуитивного интерфейса""",
            agent=self.ui_ux_designer,
            expected_output="План улучшения пользовательского опыта"
        )

from crewai import Task

class TechTasks:
    def setup_infrastructure(self):
        """Настройка технической инфраструктуры проекта"""
        return Task(
            description="""Настройте техническую инфраструктуру проекта Legion:
            1. Проверьте текущую структуру проекта
            2. Убедитесь в корректности зависимостей
            3. Настройте MCP серверы
            4. Проверьте готовность к сборке и деплою
            5. Создайте план технического развития""",
            agent=self.tech_lead,
            expected_output="Отчет о техническом состоянии проекта и план развития"
        )
    
    def implement_hero_changes(self):
        """Внедрение изменений в hero блок"""
        return Task(
            description="""Внедрите изменения в hero блок согласно требованиям:
            1. Фото должно быть размещено выше текста и форм захвата
            2. Обеспечьте корректное отображение на всех устройствах
            3. Проверьте производительность и доступность
            4. Создайте резервную копию перед изменениями""",
            agent=self.tech_lead,
            expected_output="Обновленный hero блок с улучшенной структурой"
        )
    
    def verify_build(self):
        """Проверка сборки проекта"""
        return Task(
            description="""Проверьте корректность сборки проекта:
            1. Запустите npm run build
            2. Проверьте наличие ошибок
            3. Убедитесь в корректности всех страниц
            4. Проверьте оптимизацию изображений и ресурсов""",
            agent=self.tech_lead,
            expected_output="Отчет о результатах сборки и рекомендации"
        )
    
    def setup_vercel_deployment(self):
        """Настройка деплоя на Vercel"""
        return Task(
            description="""Настройте деплой проекта на Vercel:
            1. Проверьте конфигурацию Vercel
            2. Настройте переменные окружения
            3. Проверьте готовность к деплою
            4. Создайте план автоматизации деплоя""",
            agent=self.tech_lead,
            expected_output="План деплоя на Vercel и настройки"
        )
